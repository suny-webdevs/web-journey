// Odd numbers
console.log(`\nOdd number 61 to 100\n`);
let num1 = 61
while (num1 <= 100) {
    if (num1 % 2 === 1) {
        console.log(`Odd number : ${num1}`);
    }
    num1++
}
// Even numbers
console.log(`\nEven number 78 to 98\n`);
let num2 = 78
while (num2 <= 98) {
    if (num2 % 2 === 0) {
        console.log(`Even number : ${num2}`);
    }
    num2++
}